void DAC_Cfg( DAC_t *pDAC, uint8_t chSel, uint8_t refSel, uint8_t lowPowerMode )
{
	pDAC->CTRLA = (lowPowerMode != 0) ? DAC_LPMODE_bm : 0;
	pDAC->CTRLB = chSel;
	pDAC->CTRLC = refSel;
}
